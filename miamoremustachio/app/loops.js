// "while" loop:
let i = 1;

while (i <= 19) {
  console.log(i);
  i++;
}

// "while" loop with break:
let l = 1;

while (true) {
  console.log(l++);

  if (l > 19) break;
}

// "do...while" loop:
let j = 1;

do {
  console.log(j);
  j++;

} while (j <= 19);

// "for" loop:
for (let k = 1; k <= 19; k++) {
  console.log(k);
}
